#include "libft.h"

char	*ft_strdup(char *str)
{
	char	*str2;
	size_t	len;

	len = ft_strlen(str);
	str2 = (char *)malloc(len + 1);
	if (!(str2))
	{
		return (NULL);
	}
	ft_memcpy(str2, str, len + 1);
    str[len + 1] = '\0';
	return (str2);
}
